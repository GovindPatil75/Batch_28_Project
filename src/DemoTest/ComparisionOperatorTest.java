package DemoTest;

public class ComparisionOperatorTest {

	public static void main(String[] args) {
		
		int a=5;
		int b=7;
		
		//comparision - >,< ,>=,<=,== 
		
		System.out.println(a>b); // boolean false
		System.out.println(a<b); //true

		System.out.println(a>=b); //false 
		System.out.println(a<=b); //true
		
		System.out.println(a==b); //false
	}

}
